<?php
// Define the variables
$a = 10;
$b = 20;

// Display the original values
echo "Before swapping:<br>";
echo "a = ".$a."<br>";
echo "b = ".$b."<br>";

// Swap the values
$temp = $a;
$a = $b;
$b = $temp;

// Display the new values
echo "After swapping:<br>";
echo "a = ".$a."<br>";
echo "b = ".$b."<br>";
?>
